// String - stores text, such as "Hello". String values are surrounded by double quotes
// int - stores integers (whole numbers), without decimals, such as 123 or -123    4byte
// float - stores floating point numbers, with decimals, such as 19.99 or -19.99   4 byte
// char - stores single characters, such as 'a' or 'B'. Char values are surrounded by single quotes  1 byte
// boolean - stores values with two states: true or false  4byte
// double-  8 byte

public class variables {
    public static void main(String[] args) {
        String name = "Anish";
        int number = 10;
        float decimal = 9.99f;
        double d=4.78678;
        char letter = 'H';
        boolean bool = true;

        System.out.println(name);
        System.out.println(number);
        System.out.println(decimal);
        System.out.println(letter);
        System.out.println(bool);
        System.out.println(d);
    }
}


